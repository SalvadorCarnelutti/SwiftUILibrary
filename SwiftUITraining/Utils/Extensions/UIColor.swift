import SwiftUI

extension Color {
    public static var lavender: Color {
        return Color(hex: 0xEAF6FA)
    }
    
    public static var charcoal: Color {
        return Color(hex: 0x4A4A4A)
    }
    
    public static var deepSkyBlue: Color {
        return Color(hex: 0x00ADEE)
    }
}
