import SwiftUI

public extension Color {
    static var lavender: Color {
        return Color(hex: 0xEAF6FA)
    }
    
    static var charcoal: Color {
        return Color(hex: 0x4A4A4A)
    }
    
    static var deepSkyBlue: Color {
        return Color(hex: 0x00ADEE)
    }
    
    static var atlantis: Color {
        return Color(hex: 0xA5CD39)
    }
    
    static var venetianRed: Color {
        return Color(hex: 0xD0021B)
    }
    
    static var gainsboro: Color {
        return Color(hex: 0xDDDDDD)
    }

    static var mediumTurquoise: Color {
        return Color(hex: 0x39CCCD)
    }
    
    static var babyBlue: Color {
        return Color(hex: 0x89CFF0)
    }
}
